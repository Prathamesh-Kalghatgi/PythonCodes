favfruits =["Apple","Mango","Strawberry"]
print(favfruits)
# To print a particular fruit name
print(favfruits[1])
#To replace a particular fruit
favfruits[1]="Orange"
print(favfruits)
#to add a fruit
favfruits.append("Kiwi")
print(favfruits)
#To add and change the positon of a particular fruit
favfruits.insert(1,"Dragonfruit")
print(favfruits)
#To remove an object from the list
favfruits.remove("Apple")
print(favfruits)
#To sort the list
favfruits.sort()
print(favfruits)
#To reverse the list
favfruits.reverse()
print(favfruits)
#To delete an object
print(favfruits.pop())

